BEGIN;

UPDATE profile p
SET name = CASE (p.user_id % 2)
    WHEN 0 THEN
      (ARRAY[
        'Иванов','Смирнов','Кузнецов','Попов','Васильев','Петров','Соколов','Михайлов','Новиков','Фёдоров',
        'Морозов','Волков','Алексеев','Лебедев','Семёнов','Егоров','Павлов','Козлов','Степанов','Николаев'
      ])[1 + ((p.user_id / 2) % 20)]
      || ' ' ||
      (ARRAY[
        'Александр','Дмитрий','Максим','Сергей','Андрей','Алексей','Артём','Илья','Кирилл','Михаил',
        'Никита','Матвей','Роман','Егор','Иван','Владимир','Денис'
      ])[1 + ((p.user_id * 3) % 17)]
      || ' ' ||
      (ARRAY[
        'Иванович','Петрович','Сергеевич','Александрович','Дмитриевич','Андреевич','Алексеевич','Максимович',
        'Николаевич','Владимирович','Олегович','Павлович','Борисович','Викторович','Геннадьевич','Егорович','Романович'
      ])[1 + ((p.user_id * 5) % 17)]
    ELSE
      (ARRAY[
        'Иванова','Смирнова','Кузнецова','Попова','Васильева','Петрова','Соколова','Михайлова','Новикова','Фёдорова',
        'Морозова','Волкова','Алексеева','Лебедева','Семёнова','Егорова','Павлова','Козлова','Степанова','Николаева'
      ])[1 + ((p.user_id / 2) % 20)]
      || ' ' ||
      (ARRAY[
        'Анна','Мария','Елена','Ольга','Наталья','Ирина','Татьяна','Екатерина','Надежда','Светлана',
        'Юлия','Валентина','Галина','Людмила','Вера','Лариса','Дарья','Анастасия','Полина','Виктория'
      ])[1 + ((p.user_id * 3) % 20)]
      || ' ' ||
      (ARRAY[
        'Ивановна','Петровна','Сергеевна','Александровна','Андреевна','Николаевна','Дмитриевна','Владимировна',
        'Олеговна','Павловна','Борисовна','Викторовна','Михайловна','Алексеевна','Романовна','Егоровна'
      ])[1 + ((p.user_id * 7) % 16)]
  END
FROM users u
WHERE p.user_id = u.id
  AND u.login LIKE 'bulk_stu_%';

UPDATE grades g
SET student_name = p.name
FROM profile p
JOIN users u ON p.user_id = u.id AND u.role = 'STUDENT'
WHERE u.login LIKE 'bulk_stu_%'
  AND g.student_name = ('Ученик №' || u.id::text);

COMMIT;
